public class CPattern {

	public static void main(String[] args) 
{
		for(int col=1;col<=6;col++)
		{
		System.out.print("* ");
		}
		System.out.println();
		for(int col=1;col<=4;col++)
		{
		System.out.println("* ");
		}
		for(int col=1;col<=6;col++)
		{
		System.out.print("* ");
		}
}
}
	
